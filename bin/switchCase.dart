void main() {
  var nilai = 'A';
  switch (nilai) {
    case 'A':
      print('Wow, luar biasa. anda lulus dengan sempurna');
      break;
    case 'B':
      print('selamat, anda lulus dengan baik');
      break;
    case 'C':
      print('anda lulus');
      break;
    case 'D':
      print('anda tidak lulus');
      break;
    default:
      print('mungkin anda salah jurusan');
  }
}